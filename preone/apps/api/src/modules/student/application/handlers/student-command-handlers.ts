@Injectable()
export class EnrollStudentCommandHandler implements CommandHandler<EnrollStudentCommand> {
  private static readonly TYPE = 'Student.Enroll';

  constructor(
    private readonly bus: CommandBus,
    private readonly students: StudentService,
  ) {
    bus.register(EnrollStudentCommandHandler.TYPE, this);
  }

  async handle(command: EnrollStudentCommand): Promise<{ id: string; enrollmentId: string }> {
    const { payload, metadata } = command;
    await this.students.enrollStudent(
      payload.studentId,
      { sectionId: payload.sectionId, gradeLevel: payload.gradeLevel, enrolledAt: payload.enrolledAt },
      metadata.tenantId,
      metadata.actorId,
    );
    // enrollmentId is generated by the academics module when an Enrollment row
    // is created. For Wave 3 v1, the student service sets currentSectionId on
    // the student; the academics module creates the Enrollment row in a
    // separate step (admission approval saga — Wave 3.4).
    return { id: payload.studentId, enrollmentId: '' };
  }
}

