/**
 * Student Command Handlers — CQRS write side (BTD §12.2).
 *
 * Each handler:
 *   1. Loads aggregate(s) via repository
 *   2. Mutates aggregate (pure business logic in the aggregate)
 *   3. Saves aggregate via repository
 *   4. Returns minimal result (ID only — never read models)
 *   5. Domain events dispatched after commit
 */
import { Inject, Injectable, Logger } from '@nestjs/common';

import {
  CommandBus, CommandHandler,
} from '@shared/cqrs';
import {
  ConflictException, ValidationException,
} from '@common/errors/exceptions';

import {
  AddGuardianCommand, CreateStudentCommand, EnrollStudentCommand,
  GraduateStudentCommand, PromoteStudentCommand, SetPrimaryGuardianCommand,
  TransferStudentCommand, UpdateStudentCommand, WithdrawStudentCommand,
} from '../../application/commands/student.commands';
import { StudentService } from '../services/student.service';

// ─────────────────────────────────────────────
// CreateStudentCommandHandler
// ─────────────────────────────────────────────

@Injectable()
export class CreateStudentCommandHandler implements CommandHandler<CreateStudentCommand> {
  private readonly logger = new Logger(CreateStudentCommandHandler.name);
  private static readonly TYPE = 'Student.CreateStudent';

  constructor(
    private readonly bus: CommandBus,
    private readonly students: StudentService,
  ) {
    bus.register(CreateStudentCommandHandler.TYPE, this);
  }

  async handle(command: CreateStudentCommand): Promise<{ id: string }> {
    const { payload, metadata } = command;
    const student = await this.students.createStudent(
      payload as any,
      metadata.tenantId,
      metadata.actorId,
    );
    return { id: student.id };
  }
}

